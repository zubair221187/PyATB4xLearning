# Ternary Operator

#if condition then do this
 #   else do that

#if my_age > 18 then I can watch tiktok
#    else I can't watch
my_age = int(input("Enter your age"))
print("I will go GOA" if my_age > 18 else "Can't go, Stay Home")

if my_age > 18:
    print("Go Goa")
else:
    print("Can't go, Stay Home")